package Hacker_Rank_Problems;
import java.util.*;
public class Loops{
    
    public static void main(String[] args){
        Scanner s = new Scanner(System.in);
        
        int i,n = s.nextInt();
        for(i=1;i<=10;i++){
            System.out.println(n+" x "+i+" = "+(n*i));
        }
    }
}
